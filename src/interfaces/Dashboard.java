    public void CountTrain(){
        try {
            Connection db = DbConnector.getConnection();
            Statement sql = db.createStatement();
            ResultSet rs = sql.executeQuery("select count(*) from trainList");
            
            while (rs.next()){
              trainCount = rs.getInt("count(*)");
              String strtrainCount= Integer.toString(trainCount);
              this.countTrain.setText(strtrainCount);
                
            }
            
            
        } 
      
        
        catch (Exception ex) {
           ex.printStackTrace();
        }
    
        
    }
    
