    public void TrainAvailableTableDetails(){
        
        try {
            Connection db = DbConnector.getConnection();
            Statement sql = db.createStatement();
            ResultSet rs = sql.executeQuery("select TrainNumber,TrainName,Source,Destination from trainList");
            
            while (rs.next()){
              trainNumber = rs.getInt("TrainNumber");
              trainName = rs.getString("TrainName");
              source = rs.getString("Source");
              destination = rs.getString("Destination");
              
                
                Object[] row = {trainNumber,trainName,source,destination};
                model = (DefaultTableModel)tblTrainDetails.getModel();
                model.addRow(row);
                
                
            }
            
            
        } 
      
        
        catch (Exception ex) {
           ex.printStackTrace();
        }
    }
    
    public void TrainSheetTableDetails(){
        
        try {
            Connection db = DbConnector.getConnection();
            Statement sql = db.createStatement();
            ResultSet rs = sql.executeQuery("select ts.TrainNumber,ts.ScheduleDate,ts.NoOfACSeats,tl.ACTicketFair,ts.NoOfGeneralSeats,tl.GeneralTicketFair from trainstatus as ts,trainList as tl where ts.trainNumber=tl.trainNumber");
            
            while (rs.next()){
              trainNumber = rs.getInt("TrainNumber");
              String ScheduleDate = rs.getString("ScheduleDate");
              noOfACSeats = rs.getInt("NoOfACSeats");
              Double acTicketFair = rs.getDouble("ACTicketFair");
              noOfGeneralSeats = rs.getInt("NoOfGeneralSeats");
              Double generalTicketFair = rs.getDouble("GeneralTicketFair");
              
                
                Object[] row = {trainNumber,ScheduleDate,noOfACSeats,acTicketFair,noOfGeneralSeats,generalTicketFair};
                model = (DefaultTableModel)tblTrainAvailable.getModel();
                model.addRow(row);
                
                
            }
            
            
        } 
      
        
        catch (Exception ex) {
           ex.printStackTrace();
        }
    }
    
